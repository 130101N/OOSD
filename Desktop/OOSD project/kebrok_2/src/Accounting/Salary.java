    public void display(String id){ 
            
        try{ 
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost/kebrok","root","1122"); 
            Statement st = conn.createStatement(); 
            ResultSet rs = st.executeQuery("select * from Employees where EmployeeID = "+id);
            
            
            while(rs.next()) { 
                tf1.setText(rs.getString("EmployeeID"));
                tf2.setText(rs.getString("Name"));
                tf3.setText(rs.getString("Salary"));
                cb4.setSelectedItem(rs.getString("LastPaid"));
                
            }
        }
        catch(Exception ex){ 
            JOptionPane.showMessageDialog(null, ex.getMessage()); 
        }   
    }
    
    public void submit(){
        Connection conn;
        try{ 
            conn = DriverManager.getConnection("jdbc:mysql://localhost/kebrok","root","1122"); 
            PreparedStatement st = conn.prepareStatement("update Employees set LastPaid = ? where EmployeeID = ?"); 
            st.setString(1, (String) cb4.getSelectedItem());
            st.setString(2, tf1.getText());

            int i = st.executeUpdate();
            if(i>0){
                JOptionPane.showMessageDialog(null, "Data Saved Successfully");
            }
            else{
                JOptionPane.showMessageDialog(null, "Data not Saved");
            }
        }
        catch(Exception e) { 
            System.out.println(e.getMessage()); 
        }
    }
    
    

